import Foundation
import UIKit

extension UIColor {
    struct Theme {
        struct Text {
            static let A100 = UIColor(hex: 0x000000)
            static let A68 = UIColor(hex: 0x000000).withAlphaComponent(0.68)
            static let A50 = UIColor(hex: 0x000000).withAlphaComponent(0.50)
        }
    }
}
